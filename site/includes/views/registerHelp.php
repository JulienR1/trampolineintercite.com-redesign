<section>
    <h4 class="lato normal">Instructions</h4>
    <ul>
        <li class="lato thin">1. Se créer un compte <b>parent</b>. Il est nécessaire pour inscrire tout participant.</li>
        <li class="lato thin">2. Confirmer la création du compte dans sa <b>boîte de courriels</b>.<p>(NB) Il peut être nécessaire de compléter la création de son compte au moment de la confirmation.</p></li>
        <li class="lato thin">3. Sélectionner la <b>personne à inscrire</b> dans un cours.<p>(NB) Le cas échéant, créer un compte pour son enfant avant de l'inscrire.</p></li>
        <li class="lato thin">4. Choisir le cours auquel inscrire le participant et compléter le formulaire.</li>
        <li class="lato thin">5. Effectuer le <b>paiement</b> en sélectionnant la facture émise.<p>(NB) Vérifier l'exactitude des saisies avant de poursuivre</p></li>
    </ul>

    <p class="lato bold">Il est important de procéder au paiement afin de réserver sa place dans les groupes.</p>
</section>
